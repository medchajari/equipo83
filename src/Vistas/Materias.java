        private void btnGuardarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMateriaActionPerformed
                // TODO add your handling code here:
		int anio=Integer.parseInt(txtAnio.getText());
		String nombre=txtNombre.getText();
		
	
		boolean estado = radioActiva.isEnabled();
                Materia materia = new Materia( nombre, anio , estado);
                materiaData.guardarMateria(materia);
		limpiarCampos();
		
        }//GEN-LAST:event_btnGuardarMateriaActionPerformed

        private void btnModificarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarMateriaActionPerformed
                // TODO add your handling code here:
		if (materia != null) {
                    String nombre = txtNombre.getText();
                    int anio = Integer.parseInt(txtAnio.getText());

    
                    // Obtener el estado de la materia
                    boolean estado = radioActiva.isEnabled();

                    // Obtener el objeto Materia a modificar y obtener su ID
                    Materia materiaModificada = new Materia( nombre, anio, estado);
                    materiaModificada.setIdMateria(materia.getIdMateria());  // Aquí se obtiene el ID de la materia a modificar desde el objeto materia
    
                    // Llamar al método modificarMateria de MateriaData para aplicar la modificación
                    MateriaData materiaData = new MateriaData();
                    materiaData.modificarMateria(materiaModificada);
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se ha buscado nunguna materia para modificar.");
                }
        }//GEN-LAST:event_btnModificarMateriaActionPerformed

        private void btnEliminarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMateriaActionPerformed
                 int idMateria = obtenerIdMateria();  // Suponiendo que tienes un método para obtener el ID del alumno
    
    if (idMateria != -1) {
        // Llama al método en MateriaData para eliminar la materia
        materiaData.eliminarMateria(idMateria);
                
        // Si se eliminó exitosamente, limpia los campos
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo obtener el ID de la materia para eliminar.");
    }
        }//GEN-LAST:event_btnEliminarMateriaActionPerformed


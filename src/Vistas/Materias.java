        private void btnBuscarMateriaPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMateriaPerformed
        int id = Integer.parseInt(txtId.getText());        
        materia = materiaData.buscarMateriaPorId(id);
        
        if (materia != null) {
            txtId.setText(String.valueOf(materia.getIdMateria()));
            txtNombre.setText(materia.getNombre());   
            txtAnio.setText(String.valueOf(materia.getAnio()));
            radioActiva.setSelected(materia.isActivo());
	    labIdMateria.setText(String.valueOf(materia.getIdMateria()));
        } else {
            JOptionPane.showMessageDialog(this, "Por favor verifique el ID e intente nuevamente.");
        }
        }//GEN-LAST:event_btnBuscarMateriaPerformed

